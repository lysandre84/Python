import numpy as np
import matplotlib.pyplot as plt

mu, sigma = 0, 1
data = np.random.normal(mu, sigma, 1000)

plt.hist(data, bins=30, density=True, alpha=0.6, edgecolor='black')
plt.title("Distribution Normale N(0,1)")
plt.xlabel("Valeur")
plt.ylabel("Densité")
plt.show()
