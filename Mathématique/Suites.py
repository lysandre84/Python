import math

E12=[1.0, 1.2, 1.5, 1.8, 2.2, 2.7, 3.3, 3.9, 4.7, 5.6, 6.8, 8.2]
E24=[1.0, 1.1, 1.2, 1.3, 1.5, 1.6, 1.8, 2.0, 2.2, 2.4, 2.7, 3.0, 3.3, 3.6, 3.9, 4.3, 4.7, 5.1, 5.6, 6.2, 6.8, 7.5, 8.2, 9.1]

def findNearest(rUser, eSerie) :
    nearest = math.nan  
    min = math.inf 

    for valEserie in eSerie: 
        delta=abs(rUser-valEserie)
        if delta < min:
          nearest=valEserie
        min=delta
    return nearest 


r= float(input("Saisir une valeur quelconque de résistance comprise entre 1 et 10Ω : "))

if ((r < 1) or (r >= 10)):
    print("Valeur résistance hors limites.\nBye !")
    exit()

rE12 = findNearest(r, E12)
print("Valeur la plus proche dans la serie E12 : " + str(rE12))

rE24 = findNearest(r, E24)
print("Valeur la plus proche dans la serie E24 : " + str(rE24))


