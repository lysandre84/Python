import numpy as np
import matplotlib.pyplot as plt

lambd = 1.5  # taux (lambda)
samples = np.random.exponential(1/lambd, 1000)

plt.hist(samples, bins=30, edgecolor='black', density=True)
plt.title("Loi exponentielle")
plt.xlabel("Temps")
plt.ylabel("Densit√©")
plt.show()
